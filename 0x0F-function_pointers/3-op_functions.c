#include "3-calc.h"

/**
 * op_add - a function that add two ints
 *
 * @a : int
 * @b : int
 *
 * Return: Result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function that add two ints
 *
 * @a : int
 * @b : int
 *
 * Return: Result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function that add two ints
 *
 * @a : int
 * @b : int
 *
 * Return: Result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function that add two ints
 *
 * @a : int
 * @b : int
 *
 * Return: Result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function that add two ints
 *
 * @a : int
 * @b : int
 *
 * Return: Result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
